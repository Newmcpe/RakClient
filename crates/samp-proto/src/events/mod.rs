/// A type-erased view of one event: its id, name, and the decode/encode function pointers. Built
/// once per event type via [`make_descriptor`] and collected into the dispatch registries.
#[derive(Clone, Copy)]
pub struct EventDescriptor {
    pub rpc_id: u8,
    pub event: &'static str,
    pub decode: fn(&[u8]) -> Result<Vec<FieldValue>>,
    pub encode: fn(&[FieldValue]) -> Result<Vec<u8>>,
}

/// Construct the descriptor for an event type. Exposed (not private) so the registration macros can
/// name it as a `fn`-pointer in the `inventory::submit!` static.
pub fn make_descriptor<T: EventConv + 'static>() -> EventDescriptor {
    EventDescriptor {
        rpc_id: T::RPC_ID,
        event: T::EVENT,
        decode: T::decode_values,
        encode: T::encode_values,
    }
}

/// `inventory` wrappers — each carries a constructor so registration stays a plain function pointer
/// in a `static` (no const-generic evaluation), and incoming/outgoing share no id space.
pub struct IncomingDescriptor(pub fn() -> EventDescriptor);
pub struct OutgoingDescriptor(pub fn() -> EventDescriptor);
inventory::collect!(IncomingDescriptor);
inventory::collect!(OutgoingDescriptor);

fn build_registry(
    descriptors: impl Iterator<Item = EventDescriptor>,
) -> HashMap<u8, EventDescriptor> {
    let mut map = HashMap::new();
    for descriptor in descriptors {
        let previous = map.insert(descriptor.rpc_id, descriptor);
        debug_assert!(
            previous.is_none(),
            "duplicate RPC id {} in dispatch registry",
            descriptor.rpc_id
        );
    }
    map
}

fn incoming_registry() -> &'static HashMap<u8, EventDescriptor> {
    static REGISTRY: OnceLock<HashMap<u8, EventDescriptor>> = OnceLock::new();
    REGISTRY.get_or_init(|| {
        build_registry(
            inventory::iter::<IncomingDescriptor>
                .into_iter()
                .map(|d| (d.0)()),
        )
    })
}

fn outgoing_registry() -> &'static HashMap<u8, EventDescriptor> {
    static REGISTRY: OnceLock<HashMap<u8, EventDescriptor>> = OnceLock::new();
    REGISTRY.get_or_init(|| {
        build_registry(
            inventory::iter::<OutgoingDescriptor>
                .into_iter()
                .map(|d| (d.0)()),
        )
    })
}

/// Decode an incoming RPC body to `(eventName, positionalValues)`, or `None` if the id is not a
/// known fixed-layout event. The inner `Result` reports a malformed body.
pub fn decode_incoming(id: u8, payload: &[u8]) -> Option<Result<(&'static str, Vec<FieldValue>)>> {
    let descriptor = incoming_registry().get(&id)?;
    Some((descriptor.decode)(payload).map(|values| (descriptor.event, values)))
}

/// Re-encode an incoming RPC body from positional values (a script rewrite). `None` if unknown id.
pub fn encode_incoming(id: u8, values: &[FieldValue]) -> Option<Result<Vec<u8>>> {
    let descriptor = incoming_registry().get(&id)?;
    Some((descriptor.encode)(values))
}

/// Decode an outgoing RPC body to `(eventName, positionalValues)`. `None` if unknown id.
pub fn decode_outgoing(id: u8, payload: &[u8]) -> Option<Result<(&'static str, Vec<FieldValue>)>> {
    let descriptor = outgoing_registry().get(&id)?;
    Some((descriptor.decode)(payload).map(|values| (descriptor.event, values)))
}

/// Re-encode an outgoing RPC body from positional values. `None` if unknown id.
pub fn encode_outgoing(id: u8, values: &[FieldValue]) -> Option<Result<Vec<u8>>> {
    let descriptor = outgoing_registry().get(&id)?;
    Some((descriptor.encode)(values))
}

