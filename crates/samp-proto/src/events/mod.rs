/// Generate an event struct + its `RPC_ID`/`EVENT` constants + [`WireDecode`]/[`WireEncode`] from a
/// declarative field list. Field type tokens map to the SA-MP wire primitives (see [`read_field!`]).
macro_rules! samp_event {
    (
        $(#[$meta:meta])*
        $name:ident = $id:literal, $ev:literal {
            $( $field:ident : $fty:tt ),* $(,)?
        }
    ) => {
        $(#[$meta])*
        #[derive(Debug, Clone, PartialEq)]
        pub struct $name {
            $( pub $field: field_ty!($fty), )*
        }

        impl $name {
            /// SA-MP RPC/packet id this event is carried by.
            pub const RPC_ID: u8 = $id;
            /// MoonLoader-compatible event name (the Lua callback key).
            pub const EVENT: &'static str = $ev;
        }

        impl $crate::events::WireDecode for $name {
            #[allow(unused_variables)]
            fn decode(r: &mut $crate::BitStreamReader) -> $crate::Result<Self> {
                Ok(Self {
                    $( $field: read_field!(r, $fty), )*
                })
            }
        }

        impl $crate::events::WireEncode for $name {
            #[allow(unused_variables)]
            fn encode(&self, w: &mut $crate::BitStreamWriter) {
                $( write_field!(w, self.$field, $fty); )*
            }
        }

        impl $crate::events::EventConv for $name {
            const RPC_ID: u8 = $id;
            const EVENT: &'static str = $ev;

            fn to_values(&self) -> ::std::vec::Vec<$crate::events::FieldValue> {
                ::std::vec![ $( to_value!(self.$field, $fty), )* ]
            }

            #[allow(unused_variables, unused_mut)]
            fn from_values(values: &[$crate::events::FieldValue]) -> $crate::Result<Self> {
                let mut iter = values.iter();
                Ok(Self {
                    $( $field: from_value!(iter.next(), $fty), )*
                })
            }
        }
    };
}

/// Like [`samp_event!`] but also registers the event in the incoming (server→client) dispatch table.
/// The `@register` arm is listed first so the recursive marker call never falls through to the
/// general arm (which would re-expand and recurse).
macro_rules! incoming_event {
    (@register $(#[$meta:meta])* $name:ident = $($rest:tt)* ) => {
        impl $crate::events::DirectedEvent for $name {
            const DIRECTION: $crate::Direction = $crate::Direction::Incoming;
        }
        $crate::inventory::submit! {
            $crate::events::IncomingDescriptor($crate::events::make_descriptor::<$name>)
        }
    };
    ( $($def:tt)* ) => {
        samp_event! { $($def)* }
        incoming_event!(@register $($def)*);
    };
}

/// Like [`samp_event!`] but also registers the event in the outgoing (client→server) dispatch table.
macro_rules! outgoing_event {
    (@register $(#[$meta:meta])* $name:ident = $($rest:tt)* ) => {
        impl $crate::events::DirectedEvent for $name {
            const DIRECTION: $crate::Direction = $crate::Direction::Outgoing;
        }
        $crate::inventory::submit! {
            $crate::events::OutgoingDescriptor($crate::events::make_descriptor::<$name>)
        }
    };
    ( $($def:tt)* ) => {
        samp_event! { $($def)* }
        outgoing_event!(@register $($def)*);
    };
}

