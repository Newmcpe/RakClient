/// A single decoded event field, in the order it appears on the wire. This is the dynamic bridge
/// between the typed event structs and the script layer: an event decodes to an ordered
/// `Vec<FieldValue>` (the positional callback arguments), and a script's (possibly mutated) values
/// re-encode through the same event. One `match` in the script host converts these to/from Lua,
/// rather than 152 per-struct conversions.
#[derive(Debug, Clone, PartialEq)]
pub enum FieldValue {
    Bool(bool),
    U8(u8),
    U16(u16),
    U32(u32),
    I8(i8),
    I16(i16),
    I32(i32),
    F32(f32),
    Vec3(Vector3),
    Vec2(Vector2),
    Quat(Quaternion),
    /// `str8`/`str16`/`str32`/`fixedString` — raw bytes in the server encoding.
    Bytes(Vec<u8>),
    IntArray3([i32; 3]),
}

/// Dynamic conversion between a typed event and its positional [`FieldValue`] list, plus the id and
/// MoonLoader event name. Generated by [`samp_event!`]; the registry erases the concrete type behind
/// function pointers so the host can decode by id.
pub trait EventConv: WireDecode + WireEncode + Sized {
    const RPC_ID: u8;
    const EVENT: &'static str;

    /// The event's fields as positional values, in wire order.
    fn to_values(&self) -> Vec<FieldValue>;

    /// Rebuild the event from positional values (a script's rewritten arguments). Errors on an
    /// arity or type mismatch.
    fn from_values(values: &[FieldValue]) -> Result<Self>;

    /// Decode a payload straight to its positional values.
    fn decode_values(payload: &[u8]) -> Result<Vec<FieldValue>> {
        Ok(decode_event::<Self>(payload)?.to_values())
    }

    /// Encode positional values straight to a payload.
    fn encode_values(values: &[FieldValue]) -> Result<Vec<u8>> {
        Ok(encode_event(&Self::from_values(values)?))
    }
}

/// An event tagged with its wire direction, so a registry can route `register::<T>` without a
/// direction argument. Implemented by the [`incoming_event!`]/[`outgoing_event!`] macros.
pub trait DirectedEvent: EventConv {
    const DIRECTION: crate::Direction;
}

/// Decode an event body from the argument bitstream (the id byte is already stripped).
pub trait WireDecode: Sized {
    fn decode(r: &mut BitStreamReader) -> Result<Self>;
}

/// Encode an event body back to a bitstream (used when a script rewrites a packet).
pub trait WireEncode {
    fn encode(&self, w: &mut BitStreamWriter);
}

/// Decode an event directly from a payload slice.
pub fn decode_event<T: WireDecode>(payload: &[u8]) -> Result<T> {
    let mut r = BitStreamReader::new(payload);
    T::decode(&mut r)
}

/// Encode an event to a fresh payload buffer.
pub fn encode_event<T: WireEncode>(event: &T) -> Vec<u8> {
    let mut w = BitStreamWriter::new();
    event.encode(&mut w);
    w.into_bytes()
}

// The three macros below (`read_field!`, `write_field!`, `field_ty!`) enumerate the same wire-type
// tokens in lockstep — adding or renaming a wire type means editing all three. Keep them in sync.
macro_rules! read_field {
    ($r:ident, bool) => {
        $r.read_bit()?
    };
    ($r:ident, bool8) => {
        $r.read_bool8()?
    };
    ($r:ident, bool32) => {
        $r.read_bool32()?
    };
    ($r:ident, u8) => {
        $r.read_u8()?
    };
    ($r:ident, u16) => {
        $r.read_u16()?
    };
    ($r:ident, u32) => {
        $r.read_u32()?
    };
    ($r:ident, i8) => {
        $r.read_i8()?
    };
    ($r:ident, i16) => {
        $r.read_i16()?
    };
    ($r:ident, i32) => {
        $r.read_i32()?
    };
    ($r:ident, f32) => {
        $r.read_f32()?
    };
    ($r:ident, vector3) => {
        $r.read_vector3()?
    };
    ($r:ident, vector2) => {
        $r.read_vector2()?
    };
    ($r:ident, norm_quat) => {
        $r.read_norm_quat()?
    };
    ($r:ident, compressed_vector) => {
        $r.read_compressed_vector()?
    };
    ($r:ident, str8) => {
        $r.read_str8_bytes()?
    };
    ($r:ident, str16) => {
        $r.read_str16()?
    };
    ($r:ident, str32) => {
        $r.read_str32()?
    };
    ($r:ident, fixed32) => {
        $r.read_fixed_string(32)?
    };
    ($r:ident, int_array3) => {
        [$r.read_i32()?, $r.read_i32()?, $r.read_i32()?]
    };
}

