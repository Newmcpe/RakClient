//! Typed RPC/packet event structs, ported field-for-field from `samp/events.lua` +
//! `samp/events/handlers.lua`.
//!
//! Each event is a plain wire-DTO: fields use raw integer/float types (not the domain newtypes) so
//! they map 1:1 onto the reference Lua schema and convert trivially to Lua values. The
//! [`WireDecode`]/[`WireEncode`] traits give every event a uniform body codec; the
//! [`samp_event!`] macro generates the struct, the `RPC_ID`/`EVENT` constants, and both trait impls
//! from a declarative field list.
//!
//! Events whose layout is content-dependent (give/take damage, object material vs. text) or that
//! read to the end of the stream (scores/pings, weapons update, create-object materials) are hand
//! written in the submodules rather than via the macro.
//!
//! # Why raw primitives, not the domain newtypes
//!
//! Event fields use `u16`/`i32`/`f32` rather than [`crate::PlayerId`] & friends *by policy*: these
//! are wire DTOs at the decode boundary. Raw ints make decoding tolerant of unknown/forward-compat
//! values a stricter type would reject, and map 1:1 onto both the reference Lua schema and the Lua
//! tables the bridge produces. Promotion to the domain newtypes (and closed enums for `reason`,
//! `style`, `special_action`, …) belongs in a DTO→domain conversion layer above this module, not in
//! the wire structs themselves.

